<?php

namespace App\Http\Controllers\Admin;

use App\Components\Product\Models\Product;
use App\Components\Product\Repositories\ProductRepository;
use Illuminate\Http\Request;

class ProductController extends AdminController
{

    private $productRepository;

    public function __construct(ProductRepository $productRepository)
    {
        $this->productRepository = $productRepository;
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = $this->productRepository->list(request()->all());
        $options = $this->productRepository->options();
        return $this->sendResponseOk(compact('data', 'options'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
